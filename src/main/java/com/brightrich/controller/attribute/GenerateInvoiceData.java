package com.brightrich.controller.attribute;

public class GenerateInvoiceData {

	private String errorMsg;
	private String invfileName;
	private String state;
	private String taxNum;
	private String invoiceNum;
	private String invoiceDate;
	
	public String getErrorMsg() {
		return errorMsg;
	}
	public void setErrorMsg(String errorMsg) {
		this.errorMsg = errorMsg;
	}
	public String getInvfileName() {
		return invfileName;
	}
	public void setInvfileName(String invfileName) {
		this.invfileName = invfileName;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getTaxNum() {
		return taxNum;
	}
	public void setTaxNum(String taxNum) {
		this.taxNum = taxNum;
	}
	public String getInvoiceNum() {
		return invoiceNum;
	}
	public void setInvoiceNum(String invoiceNum) {
		this.invoiceNum = invoiceNum;
	}
	public String getInvoiceDate() {
		return invoiceDate;
	}
	public void setInvoiceDate(String invoiceDate) {
		this.invoiceDate = invoiceDate;
	}
	
	
	
	
}
